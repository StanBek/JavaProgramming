package day07_Operators;

public class ArithmeticsOperators {

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 3;

        int division = num1 / num2; // division variables contains the results of num1 by num2
        int remainder = num1 % num2; // remainder variables contains the remainder of num divided by nymber

        System.out.println(num1 +" divided by " + num2 + " is equal to " + division + " with a remainder of " + remainder);

        // 10 divide by 3 is equal to 3 a remainder of 1
    }
}
