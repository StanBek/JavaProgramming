package day05_Concatenation;

public class StudentInfo {

    public static void main(String[] args) {
        /*
        2. Create a class called StudentInfo, and declare variables for the followings:
                1. student name (String)
                2. age (int)
                3. gender (char)
                4. school name (string)
                5. phone number (string)
                6. full time (Boolean)
                7. GPA = double
         */
        String studentName = "Stan";
        int age = 39;
        char gender = 'M';
        String schoolName = "Cydeo";
        String phonNumber = "312";
        boolean isFullTime = true;
        double GPA = 2.5;

        System.out.println("studentName = " + studentName);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("schoolName = " + schoolName);
        System.out.println("phonNumber = " + phonNumber);
        System.out.println("isFullTime = " + isFullTime);
        System.out.println("GPA = " + GPA);


    }
}
